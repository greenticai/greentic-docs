#!/usr/bin/env node

/**
 * Generate source-backed reference documentation for Greentic schemas and
 * formats. The generated pages are intentionally derived from neighboring
 * Greentic repositories so docs drift is easier to spot and refresh.
 *
 * Usage:
 *   node scripts/sync-reference-schemas.mjs
 */

import fs from "fs/promises";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT_DIR = path.join(__dirname, "..");
const REFERENCE_DIR = path.join(ROOT_DIR, "src", "content", "docs", "reference");
const LOCALES = ["de", "es", "id", "ja", "zh"];

const SOURCES = {
  flowSchema: path.resolve(
    ROOT_DIR,
    "..",
    "greentic-flow",
    "docs",
    "schemas",
    "ygtc.flow.schema.json",
  ),
  packFormat: path.resolve(
    ROOT_DIR,
    "..",
    "greentic-pack",
    "docs",
    "pack-format.md",
  ),
  bundleReader: path.resolve(
    ROOT_DIR,
    "..",
    "greentic-bundle",
    "crates",
    "greentic-bundle-reader",
    "src",
    "lib.rs",
  ),
  bundleReaderReadme: path.resolve(
    ROOT_DIR,
    "..",
    "greentic-bundle",
    "crates",
    "greentic-bundle-reader",
    "README.md",
  ),
  bundleCliDoc: path.resolve(ROOT_DIR, "..", "greentic-bundle", "docs", "cli.md"),
  interfacesInventory: path.resolve(
    ROOT_DIR,
    "..",
    "greentic-interfaces",
    "docs",
    "interfaces_inventory.md",
  ),
  interfacesWitRoot: path.resolve(ROOT_DIR, "..", "greentic-interfaces", "wit"),
};

function frontmatter(title, description) {
  return [
    "---",
    `title: ${title}`,
    `description: ${description}`,
    "---",
    "",
    "{/* AUTO-GENERATED by scripts/sync-reference-schemas.mjs - do not edit by hand. */}",
    "",
  ].join("\n");
}

function fenced(value, lang = "text") {
  const fence = value.includes("```") ? "````" : "```";
  return fence + lang + "\n" + value.trimEnd() + "\n" + fence;
}

function mdTable(headers, rows) {
  const header = `| ${headers.join(" | ")} |`;
  const separator = `| ${headers.map(() => "---").join(" | ")} |`;
  return [header, separator, ...rows.map((row) => `| ${row.join(" | ")} |`)].join("\n");
}

function escapeCell(value) {
  return String(value ?? "")
    .replace(/\n/g, "<br />")
    .replace(/\|/g, "\\|");
}

function stripH1(markdown) {
  return markdown.replace(/^# .+\n+/, "").trim();
}

function findDefinition(schema, pointer) {
  return pointer
    .replace(/^#\//, "")
    .split("/")
    .reduce((node, key) => node?.[key], schema);
}

function schemaType(definition) {
  if (!definition) return "";
  if (definition.enum) return definition.enum.map((entry) => `\`${entry}\``).join(", ");
  if (Array.isArray(definition.type)) return definition.type.map((entry) => `\`${entry}\``).join(", ");
  if (definition.type) return `\`${definition.type}\``;
  if (definition.$ref) return `\`${definition.$ref.replace(/^#\/\$defs\//, "")}\``;
  if (definition.anyOf) return definition.anyOf.map(schemaType).filter(Boolean).join(" or ");
  return "";
}

function extractRustStruct(source, name) {
  const match = source.match(new RegExp(`pub struct ${name}\\b`));
  if (!match) return "";
  const itemStart = source.lastIndexOf("\n", match.index) + 1;
  const start = match.index;
  const braceStart = source.indexOf("{", start);
  if (braceStart === -1) return "";

  let depth = 0;
  for (let i = braceStart; i < source.length; i++) {
    if (source[i] === "{") depth += 1;
    if (source[i] === "}") depth -= 1;
    if (depth === 0) {
      return source.slice(itemStart, i + 1).trim();
    }
  }
  return "";
}

function extractRustFnSignature(source, name) {
  const regex = new RegExp(`pub fn ${name}\\([^]+?\\) -> [^{]+`, "m");
  const match = source.match(regex);
  return match ? match[0].trim() : "";
}

function firstPatternSchema(definition) {
  const patterns = definition?.patternProperties ?? {};
  const firstKey = Object.keys(patterns)[0];
  return firstKey ? patterns[firstKey] : undefined;
}

async function listFiles(root) {
  const results = [];
  async function walk(dir) {
    for (const entry of await fs.readdir(dir, { withFileTypes: true })) {
      const fullPath = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        await walk(fullPath);
      } else if (entry.isFile()) {
        results.push(fullPath);
      }
    }
  }
  await walk(root);
  return results.sort();
}

async function renderFlowSchema() {
  const raw = await fs.readFile(SOURCES.flowSchema, "utf8");
  const schema = JSON.parse(raw);
  const required = new Set(schema.required ?? []);
  const propertyRows = Object.entries(schema.properties ?? {}).map(([name, definition]) => [
    `\`${name}\``,
    required.has(name) ? "yes" : "no",
    escapeCell(schemaType(definition)),
    escapeCell(definition.description ?? ""),
  ]);

  const nodesSchema = schema.properties?.nodes ?? {};
  const node = firstPatternSchema(nodesSchema) ?? {};
  const nodeRows = Object.entries(node.properties ?? {}).map(([name, definition]) => [
    `\`${name}\``,
    escapeCell(schemaType(definition)),
    escapeCell(definition.description ?? ""),
  ]);

  const route = node.properties?.routing?.items ?? {};
  const routeRows = Object.entries(route.properties ?? {}).map(([name, definition]) => [
    `\`${name}\``,
    escapeCell(schemaType(definition)),
    escapeCell(definition.description ?? ""),
  ]);

  const lines = [
    frontmatter("Flow YAML Schema", "Auto-generated reference for the Greentic flow YAML schema."),
    `Source schema: \`${path.relative(ROOT_DIR, SOURCES.flowSchema)}\``,
    "",
    `Schema id: \`${schema.$id}\``,
    "",
    "Greentic flow files are YAML documents validated against this JSON Schema. The current schema describes self-describing flow nodes and supports `messaging`, `webhook`, `timer`, `websocket`, and `pubsub` flow types.",
    "",
    "## Top-level document",
    "",
    mdTable(["Field", "Required", "Type", "Description"], propertyRows),
    "",
    "## Node shape",
    "",
    "Each key under `nodes` is a node id. Component operation keys use the schema pattern `^[a-zA-Z][\\w.-]*\\.[\\w.-]+$`.",
    "",
    mdTable(["Field", "Type", "Description"], nodeRows),
    "",
    "## Routing",
    "",
    mdTable(["Field", "Type", "Description"], routeRows),
    "",
    "## Generated JSON Schema",
    "",
    "<details>",
    "<summary>Full schema</summary>",
    "",
    fenced(JSON.stringify(schema, null, 2), "json"),
    "",
    "</details>",
    "",
    "## Related commands",
    "",
    "- `gtc wizard --schema` prints wizard schemas for coding agents and automation.",
    "- `greentic-flow doctor` validates flow files and pack flow definitions.",
    "- `greentic-flow component-schema` inspects component operation schemas for flow authoring.",
    "",
  ];
  return lines.join("\n");
}

async function renderPackFormat() {
  const source = await fs.readFile(SOURCES.packFormat, "utf8");
  return [
    frontmatter("Pack Format", "Auto-generated reference for the Greentic .gtpack format."),
    `Source document: \`${path.relative(ROOT_DIR, SOURCES.packFormat)}\``,
    "",
    stripH1(source),
    "",
  ].join("\n");
}

async function renderBundleFormat() {
  const [reader, readme, cliDoc] = await Promise.all([
    fs.readFile(SOURCES.bundleReader, "utf8"),
    fs.readFile(SOURCES.bundleReaderReadme, "utf8"),
    fs.readFile(SOURCES.bundleCliDoc, "utf8"),
  ]);
  const versionMatch = reader.match(/pub const BUNDLE_FORMAT_VERSION: &str = "([^"]+)";/);
  const items = [
    "BundleManifest",
    "BundleLock",
    "BundleRuntimeSurface",
    "BundleResolvedTargetView",
    "BundleResolvedReferencePolicyView",
  ]
    .map((name) => extractRustStruct(reader, name))
    .filter(Boolean)
    .join("\n\n");
  const functions = ["open_artifact", "open_build_dir"]
    .map((name) => extractRustFnSignature(reader, name))
    .filter(Boolean)
    .join("\n\n");

  const lines = [
    frontmatter("Bundle Format", "Auto-generated reference for the Greentic .gtbundle format."),
    "Source inputs:",
    "",
    `- \`${path.relative(ROOT_DIR, SOURCES.bundleReader)}\``,
    `- \`${path.relative(ROOT_DIR, SOURCES.bundleReaderReadme)}\``,
    `- \`${path.relative(ROOT_DIR, SOURCES.bundleCliDoc)}\``,
    "",
    `Current format version: \`${versionMatch?.[1] ?? "unknown"}\``,
    "",
    ".gtbundle artifacts are deterministic SquashFS bundles produced by `greentic-bundle build`. The typed reader also supports normalized unpacked build directories, which are the build-state form used before the final SquashFS artifact is materialized.",
    "",
    "## Workspace and artifact files",
    "",
    mdTable(
      ["Path", "Purpose"],
      [
        ["`bundle.yaml`", "Authored bundle workspace root."],
        ["`bundle.lock.json`", "Resolved lock contract for catalogs, app packs, extension providers, tool version, cache policy, and build markers."],
        ["`tenants/<tenant>/tenant.gmap`", "Tenant access map."],
        ["`tenants/<tenant>/teams/<team>/team.gmap`", "Team access map."],
        ["`resolved/...` and `state/resolved/...`", "Generated resolved outputs."],
        ["`state/setup/*.json`", "Bundle-owned setup state."],
        ["`state/build/<bundle>/normalized/`", "Normalized build directory that carries `bundle-manifest.json`, `bundle-lock.json`, `bundle.yaml`, materialized packs, providers, resolved files, and setup files."],
        ["`dist/<bundle>.gtbundle`", "Default final SquashFS artifact path."],
      ],
    ),
    "",
    "## Reader contract",
    "",
    stripH1(readme),
    "",
    "The reader validates basic manifest/lock structure before exposing the runtime surface. Validation checks include format version, matching `bundle_id`, matching `requested_mode`, `.gtbundle` artifact extension, expected root file names, and setup-state consistency between manifest and lock.",
    "",
    "## Typed runtime surface",
    "",
    fenced(items, "rust"),
    "",
    "## Reader entry points",
    "",
    fenced(functions, "rust"),
    "",
    "## Build/export notes",
    "",
    "The bundle CLI source document currently records these format-relevant semantics:",
    "",
    "- `build` writes normalized metadata under `state/build/<bundle>/normalized` before creating the final `.gtbundle`.",
    "- `.gtbundle` artifacts are real SquashFS artifacts produced via `mksquashfs`.",
    "- `export` consumes a normalized build directory instead of rebuilding the workspace.",
    "- `doctor` validates either a workspace or an artifact.",
    "- `inspect` reads either a workspace or an artifact and emits stable JSON.",
    "- Artifact reads use `greentic-bundle-reader` so runtime consumers do not parse raw SquashFS layout directly.",
    "",
    "## Source CLI notes",
    "",
    "This page is generated from the bundle reader plus the bundle CLI baseline document. The source baseline is intentionally not duplicated in full here; rerun the generator when the upstream `greentic-bundle` reader or CLI notes change.",
    "",
  ];
  return lines.join("\n");
}

async function renderWitInterfaces() {
  const inventory = await fs.readFile(SOURCES.interfacesInventory, "utf8");
  const witFiles = (await listFiles(SOURCES.interfacesWitRoot))
    .filter((file) => file.endsWith(".wit"))
    .map((file) => path.relative(SOURCES.interfacesWitRoot, file));

  const canonical = [
    "greentic/types-core@0.6.0/package.wit",
    "greentic/component@0.6.0/package.wit",
    "greentic/codec@0.6.0/package.wit",
    "greentic/common-types@0.1.0/package.wit",
    "greentic/component-v1@0.1.0/package.wit",
    "greentic/pack-export-v1@0.1.0/package.wit",
    "greentic/operator@1.0.0/package.wit",
    "greentic/provider/schema-core@1.0.0/package.wit",
    "greentic/secrets-store@1.0.0/package.wit",
    "greentic/state-store@1.0.0/package.wit",
    "greentic/http-client@1.1.0/package.wit",
    "greentic/telemetry-logger@1.0.0/package.wit",
  ];

  const lines = [
    frontmatter("WIT Interfaces", "Auto-generated inventory of Greentic WIT interfaces."),
    "Source inputs:",
    "",
    `- \`${path.relative(ROOT_DIR, SOURCES.interfacesInventory)}\``,
    `- \`${path.relative(ROOT_DIR, SOURCES.interfacesWitRoot)}\``,
    "",
    stripH1(inventory),
    "",
    "## Canonical package paths",
    "",
    ...canonical.map((entry) => `- \`${entry}\``),
    "",
    "## Complete WIT file inventory",
    "",
    ...witFiles.map((entry) => `- \`${entry}\``),
    "",
    "## Rendering upstream WIT docs",
    "",
    "The upstream repository includes a renderer that exports package documentation from the checked-in WIT files:",
    "",
    fenced("../greentic-interfaces/scripts/render-wit-docs.sh /tmp/greentic-wit-docs", "bash"),
    "",
    "Use the generated HTML output when you need package-level WIT documentation. Use this page as the docs-site inventory and pointer to the authoritative `greentic-interfaces` files.",
    "",
  ];
  return lines.join("\n");
}

async function writeReference(relativePath, content) {
  const target = path.join(REFERENCE_DIR, relativePath);
  await fs.mkdir(path.dirname(target), { recursive: true });
  await fs.writeFile(target, content, "utf8");
  console.log(`[sync-reference-schemas] wrote ${path.relative(ROOT_DIR, target)}`);

  for (const locale of LOCALES) {
    const localized = path.join(
      ROOT_DIR,
      "src",
      "content",
      "docs",
      locale,
      "reference",
      relativePath,
    );
    await fs.mkdir(path.dirname(localized), { recursive: true });
    await fs.writeFile(localized, content, "utf8");
    console.log(`[sync-reference-schemas] wrote ${path.relative(ROOT_DIR, localized)}`);
  }
}

async function main() {
  await writeReference("flow-schema.md", await renderFlowSchema());
  await writeReference("pack-format.md", await renderPackFormat());
  await writeReference("bundle-format.md", await renderBundleFormat());
  await writeReference("wit-interfaces.md", await renderWitInterfaces());
}

main().catch((error) => {
  console.error(`FAIL: ${error.message}`);
  process.exit(1);
});
